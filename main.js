
//Equality (a == b)
let numeroa = 8, numerob = 8;
let resultado = numeroa == numerob;
console.log(`${numeroa} == ${numerob} ? ${resultado}`);

/* Inequality (a != b)*/
numeroa = 6, numerob = "6"
resultado = numeroa != numerob;
console.log(`${numeroa} != '${numerob}' ? ${resultado}`);

/* Identity (a === b) */
numeroa = 10 , numerob = "10"
resultado = numeroa === numerob;
console.log(`${numeroa} === '${numerob}' ? ${resultado}`);

/* Non-identity (a !== b)*/
numeroa = 86 , numerob = "86"
resultado = numeroa !== numerob;
console.log(`${numeroa} !== '${numerob}' ? ${resultado}`);

/* Greater than (a > b) */
numeroa = 10 , numerob = 36
resultado = numeroa > numerob;
console.log(`${numeroa} > ${numerob} ? ${resultado}`);

/* Greater than or equal (a >= b)*/
numeroa = 10 , numerob = 2
resultado = numeroa >= numerob;
console.log(`${numeroa} >= ${numerob} ? ${resultado}`);

/* Less than (a < b) */
numeroa = 10 , numerob = 87
resultado = numeroa < numerob;
console.log(`${numeroa} < ${numerob} ? ${resultado}`);

/* Less than or equal (a <= b) */
numeroa = 10 , numerob = "10"
resultado = numeroa <= numerob;
console.log(`${numeroa} <= '${numerob}' ? ${resultado}`);

