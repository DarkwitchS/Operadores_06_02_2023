let num1 = 10, num2 = 3;
let resultado;

//suma
resultado = num1 + num2;
console.log(`${num1} + ${num2} = ${resultado}`);

//resta
resultado = num1 - num2;
console.log(`${num1} - ${num2} = ${resultado}`);

//multiplicación
resultado = num1 * num2;
console.log(`${num1} * ${num2} = ${resultado}`);

//división
resultado = num1 / num2;
console.log(`${num1} / ${num2} = ${resultado}`);

//Exponenciación
resultado = num1 ** num2;
console.log(`${num1} ^ ${num2} = ${resultado}`);

//Residuo (%)
resultado = num1 % num2;
console.log(`${num1} % ${num2} = ${resultado}`);

//Unary negation
console.log(`${-num1} , ${-num2} `);

//Unary plus
console.log(`${+num1}  ${+num2} `);